user_input=float(input("Enter a floating-point number: "))

int_value= int(user_input)

print("\nConversion Results:")
print(f"Original floating-point number: {user_input}")
print(f"Converted integer value: {int_value}")
print("Explanation: Converting a floating-point number to an integer using int() removes the decimal part, effectively rounding down to the nearest whole number.")
